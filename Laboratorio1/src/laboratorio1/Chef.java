    public static void leerFichero(BufferedReader br) throws Exception {
        String linea;
        int sw = 0;
        int z = 0;
        linea = br.readLine();
        String plato;
        String ingr = "";
        while (linea != null) {
            sw = 0;
            for (int i = 0; i < linea.length(); i++) {
                System.out.println("ChatAt(i): " + linea.charAt(i));
                if (linea.charAt(i) == ',' && sw == 0) {
                    plato = linea.substring(0, i);
                    ChefTabla.setValueAt(plato, z, 0);
                    sw = 1;
                } else if (sw == 1) {
                    ingr = linea.substring(i, linea.length());
                    ChefTabla.setValueAt(ingr, z, 1);
                    sw = 2;
                    z++;
                }
            }
            linea = br.readLine();
        }
    }
    
