    public static void crearFichero() {
        FileWriter fw = null;
        try {

            File archivo = new File("platos.txt");

            if (archivo.exists()) {
                s = 1;
            } else {
                fw = new FileWriter("platos.txt");
                PrintWriter pw = new PrintWriter(fw);
                escribirFichero(pw);
                s = 1;
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        } finally {
            try {
                if (fw != null) {
                    fw.close();
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }

    public static void escribirFichero(PrintWriter pw) throws Exception {
        filas = Integer.parseInt(tf1.getText());
        String opcion;
        int z = 0;
        opcion = NombreTF.getText();
        pw.print(opcion + ", ");
        for (int i = 0; i < filas - 1; i++) {
            opcion = IngTabla.getValueAt(z, 0).toString();
            pw.print(opcion + ", ");
            z++;
        }
        if (z == filas - 1) {
            opcion = IngTabla.getValueAt(z, 0).toString();
            pw.print(opcion);
            z++;
        }
        if (PrecioTF.getText() != null) {
            opcion = PrecioTF.getText();
            pw.print(", "+opcion);
        }
        pw.println();
    }

